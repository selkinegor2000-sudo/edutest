function heading(text, level = HeadingLevel.HEADING_1) {
  return new Paragraph({ text, heading: level, spacing: { after: 160 } });
}

function body(text, options = {}) {
  return new Paragraph({ text, spacing: { after: 120 }, alignment: AlignmentType.JUSTIFIED, ...options });
}

function bullet(text, level = 0) {
  return new Paragraph({ text, bullet: { level }, spacing: { after: 80 } });
}

function signatureLine(label) {
  return new Paragraph({
    children: [
      new TextRun({ text: `${label}: `, bold: true }),
      new TextRun("____________________________"),
    ],
    spacing: { after: 140 },
  });
}

